// # JavaScript – Basic Notes

// 1. JavaScript (JS) is a lightweight, interpreted programming language used to create interactive and dynamic web pages.

// 2. JavaScript is one of the three core technologies of web development:

//    * HTML – Structure
//    * CSS – Styling
//    * JavaScript – Functionality and Interactivity

// 3. JavaScript is a high-level, dynamic, and prototype-based language.

// 4. It supports multiple programming paradigms:

//    * Object-Oriented Programming (OOP)
//    * Functional Programming
//    * Imperative Programming

// 5. JavaScript is mainly used for:

//    * Web Development
//    * Server-side Development (Node.js)
//    * Mobile Applications
//    * Desktop Applications
//    * Game Development

// 6. JavaScript can run in:

//    * Web Browsers
//    * Node.js
//    * Apache CouchDB
//    * Adobe Acrobat

// 7. JavaScript is standardized by ECMAScript (ECMA-262).

// 8. JavaScript is different from Java. Both are separate programming languages with different syntax and uses.

// 9. JavaScript is case-sensitive.
//    Example:

//    * myVariable and MyVariable are different variables.

// 10. JavaScript uses a garbage collector to automatically manage memory.

// 11. Functions in JavaScript are first-class citizens, which means they can be stored in variables, passed as arguments, and returned from other functions.

// 12. JavaScript supports dynamic typing, meaning the data type of a variable can change during execution.

// 13. JavaScript can create objects and functions at runtime.

// 14. JavaScript is an event-driven language, meaning it responds to events such as clicks, key presses, and mouse movements.

// 15. JavaScript can manipulate HTML and CSS using the Document Object Model (DOM).

// 16. Variables in JavaScript can be declared using:

    // * var
    // * let
    // * const

// 17. JavaScript provides several data types:

    // * Number
    // * String
    // * Boolean
    // * Null
        // undefined is assigned by JavaScript automatically, while null is assigned by the programmer intentionally.
    // * Undefined
    // * Object
    // * Symbol
    // * BigInt

// 18. JavaScript supports loops and conditional statements:

    // * if, else
    // * switch
    // * for
    // * while
    // * do-while
    // * for...in
    // * for...of
    // * forEach (for arrays)
    // * break and continue
    // * try...catch for error handling
    // * throw for throwing exceptions
    // * finally for executing code after try...catch

// 19. JavaScript supports arrays and objects for storing collections of data.

// 20. Popular JavaScript frameworks and libraries include:

    // * React
    // * Angular
    // * Vue
    // * Express.js

// # Frequently Asked Interview Questions

// 1. What is JavaScript?
//    JavaScript is a scripting language used to make web pages interactive and dynamic.

// 2. Is JavaScript the same as Java?
//    No, JavaScript and Java are completely different programming languages.

// 3. Is JavaScript case-sensitive?
//    Yes, JavaScript is case-sensitive.

// 4. Which keyword is preferred for declaring variables?
//    let and const are preferred over var.

// 5. What are the primitive data types in JavaScript?
//    Number, String, Boolean, Null, Undefined, Symbol, and BigInt.

// 6. What is the difference between let, const, and var?

//    * var: Function-scoped and can be redeclared.
//    * let: Block-scoped and can be reassigned.
//    * const: Block-scoped and cannot be reassigned.

// 7. What is the DOM?
//    DOM (Document Object Model) allows JavaScript to access and modify HTML and CSS elements.

// 8. What are arrays and objects?
//    Arrays store ordered collections of values, while objects store data in key-value pairs.

// 9. What is Node.js?
//    Node.js is a runtime environment that allows JavaScript to run outside the browser.

// 10. Why is JavaScript important?
//     JavaScript is essential for building modern, interactive, and responsive web applications.